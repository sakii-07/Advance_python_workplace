# DML commands
-- 1) Insert - data insert into table
-- 2) Update - update existing data
-- 3) delete - remove data

-- syntax 
		-- single data insert
				-- insert into table_name (col1,col2,col3, ...) values (val1,val2,val3 ....);
                
		-- multiple data insert 
				-- insert into table_name (col1,col2,col3, ...) values (val1,val2,val3 ....),
                -- (val1,val2,val3 ....),
                -- (val1,val2,val3 ....),
                --  (val1,val2,val3 ....);